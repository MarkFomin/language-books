/*
 * Простой пример обработки исключения.
 */
#include<iostream>
using namespace std;
int main(){
	cout<<"Старт\n";
	try {//начинается блок try
		cout<<"Внутри блока try\n";
		throw 99;//выбросим ошибку 
		cout<<"Это выполняться не будет\n";
	} catch (int i) {//поймали ошибку
		cout<<"Поймано исключение - значение равно: "<<i<<"\n";
	}
	cout<<"Завершение\n";
	return 0;
}
