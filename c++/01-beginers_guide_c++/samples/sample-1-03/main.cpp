//Использование оператора.

#include <iostream>
using namespace std;

int main()
{
	int length; //объявляение переменной
	int width; //объявляение другой переменной
	int area; //объявляение тертьей переменной
	length = 7; //здесь length присваивается значение 7
	width = 5; //здесь width присваивается значение 5
	area = length*width; //вычисление площади
	cout << " Площадь равна ";
	cout << area; //здесь выводится 35
	cout << "\n";
	
	return 0;
}
